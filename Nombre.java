public class Nombre {
    private Float valeur;
    public Nombre(Float valeur) {
        this.valeur = valeur;
    }
    public Nombre() {
        this.valeur = null;
    }
    public void setValeur(Float valeur) {
        this.valeur = valeur;
    }
    public Float getValeur() {
        return this.valeur;
    }

}
